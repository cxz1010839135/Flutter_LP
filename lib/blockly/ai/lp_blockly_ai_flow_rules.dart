  /// 注入 AI system prompt 的流程架构与生成规则。
  static String buildFlowRulesSection() {
    return '''
## 触摸屏驱控工程流程规则（G代码触摸屏改造-0425 参考，生成时必须遵守）

### 一、工程类型（先判断再生成）

**A. 工站驱控（IP100/103/110/120/130 模式）**
- 每扫描周期：先判断 `MOFF.999==1` 走初始化链，否则走业务
- 初始化链（串行 procedures_call）：`初始化数据` → `输入IO` → `IO输出` → `报警部分` → `PVT标志位` → `一键复位S0` → `按键控制_改`
- `按键控制_改` 内：`MOFF.11==1` 手动（`手动JOG`+`手动PVT部分`+`手动IO`），否则自动（串行调用各 `Sxx自动流程-…`）

**B. HMI/触摸屏主控（IP101 模式）**
- 无 MOFF.999 门控；每周期扁平串行调用全部函数
- 典型函数：`主程序`、`运行标志`、`输入转换`、`输出转换`、`轴报警`、`光栅/安全门/气缸报警`、`报警转换/蜂鸣器`、`上位机…交互`、`通讯同步`
- 运动块极少；职责是 M/S/D 与触摸屏 D 寄存器映射

**C. 从站精简（IP121 模式）**
- 仅保留：`通讯同步`、`初始化数据`、`报警部分`、`PVT标志位`、`手动JOG&&手动PVT`、`S10自动流程-…`
- 根据主站信号做位置判定，不重复主站完整流程

### 二、函数命名规范

| 类别 | 命名格式 | 示例 |
|------|----------|------|
| IO 映射 | `输入IO` / `IO输出` / `手动IO` | M1000←X0；Y0=M2000 |
| 手自动调度 | `按键控制_改` | 内含手动/自动分支 |
| 手动三件套 | `手动JOG` / `手动PVT部分` / `手动IO` | MOFF.11==1 时调用 |
| 自动流程 | `S{编号}自动流程-{工艺描述}` | `S10自动流程-空盘上盘流程` |
| 多工位拆分 | `S{编号}自动{工艺}-工作位{n}` | `S12自动叠盘储盘-工作位1` |
| 复位 | `一键复位S0` / `一键复位` | S0 步序状态机 |
| HMI 映射 | `上位机{类型}{源范围}-D{目标范围}交互` | `上位机流程S0-S10-D4000-D4009交互` |
| 通讯 | `通讯同步` / `通讯同步--数据上传寄存器` | D400-D438 监控 |

### 三、S 步序状态机（自动流程核心 — 与 IP130 等参考工程一致）

**子流程不是一个大函数写到底**，常见拆法：
1. **调度子流程** `S12自动流程-隔片上料流程-判断料仓`：入口 S==1→10，读 HMI 参数 D3312 等分支，置 M630/M631 等标志
2. **分支子流程** `S12自动流程-左料仓` / `S12自动流程-右料仓`：共用 S12，但步号段不同（如左料仓用 20~39，右料仓用 40~59）
3. **主工艺子流程** `S10自动流程-空盘上盘流程`：完整工艺，步号 10 起递增
4. **握手子流程** `S18自动流程-要料信号和放隔片信号处理流程`：跨流程 M 信号

在 `按键控制_改` 自动 ELSE 分支中**每周期串行 procedures_call 全部子流程**（不是 if 选一个）。

每个子流程函数内部**不用 while**，用链式 `controls_if` + `<next>`：

1. **入口**（通常在调度函数）：`S[n]==1` AND `M16==1` → 置 `M21=1`、`M700=1`（等）、`S[n]=10`
2. **M21/T8 超时段**（参考工程标配）：`M21==1` 时 `T8=9999999`；`MDN.21` 时 `D310=T8`
3. **逐步**：每步 `controls_if` 条件为 **S[n]==步号 AND M16==1 AND（可选 M标志/D参数/X传感器）**
4. **步内动作**：先 `math_variableNotes` 注释 → 写 M/Y（常经 M300 段映射 Y）→ `motion_move_go`（轴号、G90、距离 D、速度 D）→ 赋 `S[n]=下一步`
5. **步内可嵌套** `controls_if` 做报警分支（如 XOFF.24 AND MOFF.40 → M520=1）
6. **结束**：`S[n]=0` 或跳步；通知下游用 M7xx 握手

复合条件：`logic_operation_m_vertical`，items = 条件数-1，每步通常至少 S==步号 + M16。

### 三-1、motion_move_go 写法（参考工程）

```
motion_move_go：轴号=3，G90绝对，距离=D200，速度=D126
```

### 三-2、输出写法（参考工程）

优先写 **M309/M317/M3xx** 等内部位（由 IO输出 函数映射 Y），不要直接写 Y 除非用户明确要求。

### 四、寄存器约定（跨文件共性）

| 地址 | 用途 |
|------|------|
| MOFF.999 | 首次扫描/初始化标志 |
| MOFF.11 | 1=手动，0=自动 |
| MOFF.16 | 暂停允许复位 |
| M10 | 复位进行中 |
| M11 | 复位完成/可启动 |
| M16 | 自动使能 |
| M500 | 总报警 |
| M700+ | 工艺中间标志（满盘/空盘/要料等） |
| M1000+ | 输入 X 镜像 |
| M2000+ | 手动 IO 触发位 |
| M300+ | 输出缓冲 → Y |
| S0 | 复位流程步序 |
| S10/S12/S14/S16/S18/S20… | 各自动流程步序（偶数递增常见） |
| D10/D12/D14 | 脉冲/mm 换算系数 |
| D400-D438 | HMI 监控字（IP101） |
| D4000-D4009 | S 步序上传触摸屏 |
| D4060/D4061 | M 状态上传 |
| D4090-D4095 | 报警上传 |
| D4508 | 运行状态：1=运行，2=停机 |
| D9400/D9500 | 轴报警源 |
| T8 | 流程超时（初始化常写 9999999） |

### 五、手动 JOG 模式

- `MUP.{100+轴号}` 按下 → `motion_move_go` mutation `s_jog=1`，速度读 `D[速度参数]`
- `MDN.{100+轴号}` 松开 → `motion_move_go` mutation `t_jog=1` 停止
- 反向：速度取反 `D[0] = -1 * D[100]`

### 六、报警链模式

- 轴报警：`D9400!=0` OR `D9500!=1` → `M500=1`，否则 `M500=0`
- 多工站：每个驱控写独立 M 报警位，HMI 汇总显示
- `controls_if` 带 `mutation else="1"` 做二选一赋值

### 七、IO 映射（与快速通道一致）

- 本体输入：M1000←X0（24 点）；扩展输入 IO-n：M(1000+100n)←X(100n)（16 点）
- 本体输出：Y0=M2000（24 点）；扩展输出 IO-n：Y(100n)=M(2000+100n)（16 点）
- 手动 IO：↑M(目标+50) 触发，M目标 翻转

### 八、生成 Blockly 时的结构约束

- 每个工艺块用 `procedures_defnoreturn` 独立函数，`collapsed=true`
- 函数内用 `<next>` 链式 `controls_if`，**禁止** elseif/else-if 多分支块
- 条件体仅 IF0+DO0；需要否则用 `mutation else="1"` + ELSE
- 复合条件一律 `logic_operation_m_vertical` + AND
- 顶层主循环也是一个 `controls_if`（MOFF.999）或扁平 call 链（IP101）

### 九、用户应如何描述需求（Agent 解析指引）

当用户说「写/生成/追加 + 流程/自动/工位/复位/报警/HMI」时，按下列模板补全：

**新建工站驱控骨架**
> 生成工站主程序：MOFF.999 初始化链（初始化数据、输入IO、IO输出、报警部分、PVT标志位、一键复位S0、按键控制_改）；按键控制_改 内手动调用 JOG/PVT/手动IO，自动调用 [流程列表]

**单个自动流程**
> 生成 S{编号}自动流程-{名称}：入口 S{n}==1 且 M16==1 置 S{n}=10；步骤：①[条件]→[动作]→S{n}=11 ②… ③结束 S{n}=0

**步序表格式（推荐用户这样写）**
```
S12自动流程-隔片上料：
  10: 等待 M700==1（要料）
  11: Y5=1 气缸伸出，等待 X10==1
  12: motion_move_go 轴2到 D140 位置
  13: 等待到位 D9300==1，S12=0 完成
```

**HMI 映射**
> 生成上位机流程交互：把 S0,S2,S4…S10 映射到 D4000-D4009；或 M0-M21 映射 D4060/D4061

**报警扩展**
> 在报警部分增加：D9400!=0 时 M{工站报警位}=1，并写入 D4090 上传

**手动 IO/JOG**
> 手动IO：MUP.2050 翻转 M2000；手动JOG：MUP.100 轴0正向 D0 速度

### 十、待做需求映射（触摸屏改造）

- D4508 运行/停机 → HMI 主程序或运行标志函数内写 D4508
- 4 工站各 M 报警 → 各驱控 `报警部分` 拆分 M 位
- 储料密码/清料模式 → `按键控制_改` 或 `主程序` 加 D/M 条件
- 放盘/叠盘失败选择 → S 步序分支 + HMI 按钮 M 位等待
''';
  }

